/**
 * Ручные правки Фин. результата по запросу пользователя:
 *
 * DELETE:
 *   id=22  PO2024-2494 R0
 *   id=30  PO2025-2478 R0
 *   id=38  счет №15 (ЛЕВЕЛ 410)
 *   id=41  GS002
 *   id=67  PO13052025 дубликат (PT231669)
 *   id=68  PO13052025 дубликат (PT231670)
 *   id=77  Авиэйшн Сервис Техник
 *
 * CANCEL (status='cancelled'):
 *   id=181 2025-4207 R0
 *   id=39  счет№10 AllianceJet  (+ дата 10.03.2025 уже верная)
 *   id=13  PO2024-2339 R0
 *   id=21  PO2024-2339 R0
 *
 * COMPLETE (status='completed'):
 *   id=51  счет №21  →  дата 09.04.2025
 *
 * FIX PO13052025 (id=66):
 *   - order_date = '2025-05-13'
 *   - объединить supplier_po, supplier, supplier_amounts
 */
require("dotenv").config({ path: require("path").join(__dirname, "..", ".env") });
const path = require("path");
const fs   = require("fs");
const { pool } = require("../server/db.cjs");

const TO_DELETE  = [22, 30, 38, 41, 67, 68, 77];
const TO_CANCEL  = [181, 39, 13, 21];
const TO_COMPLETE = [{ id: 51, date: "2025-04-09" }];

async function main() {
  const client = await pool.connect();
  try {
    await client.query("BEGIN");

    // ── DELETE ──────────────────────────────────────────────────────────────
    for (const id of TO_DELETE) {
      const r = await client.query("DELETE FROM fin_results WHERE id=$1 RETURNING id, customer_po, customer", [id]);
      if (r.rowCount) console.log(`🗑  Удалён id=${r.rows[0].id}  ${r.rows[0].customer_po}  ${r.rows[0].customer}`);
      else console.warn(`⚠  id=${id} не найден`);
    }

    // ── CANCEL ───────────────────────────────────────────────────────────────
    for (const id of TO_CANCEL) {
      const r = await client.query(
        "UPDATE fin_results SET status='cancelled', force_closed=false WHERE id=$1 RETURNING id, customer_po",
        [id]
      );
      if (r.rowCount) console.log(`🔴 Отменён id=${r.rows[0].id}  ${r.rows[0].customer_po}`);
      else console.warn(`⚠  id=${id} не найден`);
    }

    // ── COMPLETE ─────────────────────────────────────────────────────────────
    for (const { id, date } of TO_COMPLETE) {
      const r = await client.query(
        "UPDATE fin_results SET status='completed', order_date=$2, force_closed=true WHERE id=$1 RETURNING id, customer_po",
        [id, date]
      );
      if (r.rowCount) console.log(`✅ Выполнен id=${r.rows[0].id}  ${r.rows[0].customer_po}  дата=${date}`);
      else console.warn(`⚠  id=${id} не найден`);
    }

    // ── FIX PO13052025 (id=66) ───────────────────────────────────────────────
    const r66 = await client.query(
      `UPDATE fin_results SET
         order_date       = '2025-05-13',
         supplier_po      = 'PT231668\nPT231669\nPT231670',
         supplier         = 'Rotor Parts Network\nZiegler Aerospace\nGLOBAL AVIATION HELICOPTER',
         supplier_amounts = '1007.82\n4490\n19995',
         supplier_amount  = 25492.82,
         force_closed     = true
       WHERE id = 66
       RETURNING id, customer_po, order_date`,
      []
    );
    if (r66.rowCount) console.log(`🔧 PO13052025 id=66 обновлён: дата=${r66.rows[0].order_date}`);
    else console.warn("⚠  id=66 не найден");

    await client.query("COMMIT");
    console.log("\n✅ Все изменения применены");
  } catch (err) {
    await client.query("ROLLBACK");
    throw err;
  } finally {
    client.release();
  }

  // ── REGENERATE finData.js ────────────────────────────────────────────────
  console.log("\n📝 Обновляю finData.js...");
  const { rows: allFin } = await pool.query(
    `SELECT * FROM fin_results ORDER BY
       CASE type WHEN 'domestic' THEN 0 ELSE 1 END,
       CASE WHEN order_date ~ '^\\d{4}-\\d{2}-\\d{2}$' THEN order_date::date ELSE NULL END NULLS LAST,
       id`
  );

  const toCamel = (s) => s.replace(/_([a-z])/g, (_, c) => c.toUpperCase());
  const rows2js = (rows) =>
    rows
      .map((r) => {
        const obj = {};
        for (const [k, v] of Object.entries(r)) {
          obj[toCamel(k)] = v === null ? null : v;
        }
        return obj;
      });

  const domestic = rows2js(allFin.filter((r) => r.type === "domestic"));
  const exported = rows2js(allFin.filter((r) => r.type === "export"));

  const outPath = path.join(__dirname, "..", "src", "data", "finData.js");
  const content = `// Auto-generated by _patch-fin-manual.cjs — ${new Date().toISOString()}
export const finDomestic = ${JSON.stringify(domestic, null, 2)};

export const finExport = ${JSON.stringify(exported, null, 2)};
`;
  fs.writeFileSync(outPath, content, "utf8");
  console.log(`✅ finData.js записан: domestic=${domestic.length}, export=${exported.length}`);

  await pool.end();
}

main().catch((e) => { console.error("Ошибка:", e.message); process.exit(1); });
